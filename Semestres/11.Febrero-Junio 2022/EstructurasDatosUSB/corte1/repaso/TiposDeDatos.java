public class TiposDeDatos{
    public static void main(String[] args){
        int variable = Integer.SIZE;
        System.out.println(variable);

        long variableLong = Long.SIZE;
        System.out.println(variableLong);

        float variableFloat = Float.SIZE;
        System.out.println(variableFloat);

        double variableDouble = Double.SIZE;
        System.out.println(variableDouble);

        System.out.println(1/3.0f);
        System.out.println(1/3.0d);
    }
}